    public ReportJDBCD(String fileName, HashMap parameter) {
        this(null, true);
        try {
            call_connection.dbConnection();
            JasperReport jr = JasperCompileManager.compileReport(fileName);//compile
            JasperPrint print = JasperFillManager.fillReport(jr, parameter, call_connection.getCon());  
            JRViewer viewer = new JRViewer(print);
            viewer.setZoomRatio((float) 0.75);
            Container c = getContentPane();
            c.setLayout(new BorderLayout());
            c.add(viewer);
        } catch (JRException jre) {
            jre.printStackTrace();
        }
    }

